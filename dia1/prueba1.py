variable1 = 8 #  variable para almacenar una cadena de caracter
variable2 = 7 #  variable para almacenar una cadena de caracter
print(variable1 + variable2)#  variable para almacenar una cadena de caracter

print("{} + {} =".format(variable1, variable2),(variable1 + variable2))  #  variable para almacenar una cadena de caracter
print("{} * {} =".format(variable1, variable2),(variable1 * variable2))  #  variable para almacenar una cadena de caracter
print("{} / {} =".format(variable1, variable2),(variable1 / variable2))  #  variable para almacenar una cadena de caracter
print("{} + {} =".format(variable1, variable2),(variable1 ** variable2))  #  variable para almacenar una cadena de caracter
print("{} + {} =".format(variable1, variable2),(variable1 % variable2))  #  variable para almacenar una cadena de caracter